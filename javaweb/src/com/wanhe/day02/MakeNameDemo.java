/*
标识符:就是给类，接口，方法，变量等起名字
组成规则：
    A.英文字母大小写
    B.数字字符  Demo01  01demo
    C.$和_

注意事项：
    A.不能以数字开头
    B.不能是java中的关键字
    C.java语言严格区分大小写


命名规则：见名知意
包：全部都是小写
类或者接口：
    一个单词：单词的首字母必须大写
    举例：Student
    多个单词：每个单词的首字母必须大写
    举例：MakeNameDemo,HelloWorld

方法或者变量：
    一个单词：单词的首字母小写
    举例：main() age
    多个单词：从第二个单词开始，每个单词的首字母大写
    举例:studentAge,showAllNames()

常量：
    一个单词：全部大写
    举例：PI
    多个单词：全部大写，每个单词用_隔开
    举例：STUDENT_MAX_AGE_NAME
*/

